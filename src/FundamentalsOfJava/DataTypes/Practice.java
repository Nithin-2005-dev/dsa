package FundamentalsOfJava.DataTypes;

public class Practice {
    public static void main(String[] args) {
        //Primitive datatypes
        //integer
        int num=10;
        System.out.println(num);
        //character
        char letter='e';
        System.out.println(letter);
        //float
        float marks=0.5f;
        System.out.println(marks);
        //double
        double LargeNumber=100000000000000000000.5555555;
        System.out.println(LargeNumber);
        //long
        long largeInteger=100000000000000000L;
        System.out.println(largeInteger);
        //boolean
        boolean check=true;
        System.out.println(check);
        //Integer wrapper class
        Integer numClass=10;
        System.out.println(numClass.toString().charAt(0));

    }
}
